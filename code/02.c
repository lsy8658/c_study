#include <stdio.h>

int main () {
   char str[100];
   printf("문자열을 입력하세요.\n");
   gets(str);
   // printf("입력한 문자열 = %s", str);
   printf("입력한 문자열 = ");
   puts(str);
   puts("KNOU");
}

